package com.commandcenter.model.smp;

public class SmpRoleFunctionInfo {
    private String code;
    private String functionGuid;
    private String roleGuid;
    private String guid;
    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getFunctionGuid() {
        return functionGuid;
    }

    public void setFunctionGuid(String functionGuid) {
        this.functionGuid = functionGuid;
    }

    public String getRoleGuid() {
        return roleGuid;
    }

    public void setRoleGuid(String roleGuid) {
        this.roleGuid = roleGuid;
    }

    public String getGuid() {
        return guid;
    }

    public void setGuid(String guid) {
        this.guid = guid;
    }


}
